let n1 = 10;
let n2 = 20;
let op = "/";
let result

switch (op){
    case "+":
        result = n1 + n2
        console.log(result)
    break;
    case "-":
        result = n1 - n2
        console.log(result)
    break;
    case "*":
        result = n1 * n2
        console.log(result)
    break;
    case "/":
        result = n1 / n2
        console.log(result)
    break;
    default:
        console.log("[Erro]")
}